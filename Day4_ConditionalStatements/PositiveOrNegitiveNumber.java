package Day4_ConditionalStatements;

public class PositiveOrNegitiveNumber {

	public static void main(String[] args) {
		
		int num=-2;
		
		if(num>0)
		{
			System.out.println("Positive Number");
		}
		else if(num<0)
		{
			System.out.println("Negative Number");
		}
		else
		{
			System.out.println("zero");
		}
	}

}
